package SeminarExceptions.HomeWork3.Task3;

public class Pow {
    /**
     * Метод, который выполняет возведение в степень.
     *
     * @param a основание.
     * @param b показатель.
     * @return Результат возведения в степень.
     */
    public int pow(int a, int b) {
        return (int) Math.pow(a, b);
    }
}
